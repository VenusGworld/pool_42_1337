int	ft_recursive_factorial(int nb)
{
	int	fact;

	fact = 1;
	if (nb < 0)
		return (0);
	if (nb == 0 || nb == 1)
		return (1);
	if (nb >= 2)
	{
		fact = nb * ft_recursive_factorial(nb - 1);
	}
	return (fact);
}
